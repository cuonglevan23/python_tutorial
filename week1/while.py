# vong lặp while - while loop
# Vòng lặp while trong Python cho phép lặp lại một khối mã cho đến khi điều kiện trở thành False.
# Vòng lặp này thường được sử dụng khi bạn không biết trước số lần lặp, mà chỉ biết điều kiện dừng.
# Khác gì so với vòng lặp for?
# Vòng lặp for thường được sử dụng khi bạn biết trước số lần lặp hoặc khi bạn muốn lặp qua một chuỗi, danh sách, tuple, hoặc dictionary.
# Vòng lặp while thường được sử dụng khi bạn không biết trước số lần lặp, mà chỉ biết điều kiện dừng.
# Vòng lặp while sẽ tiếp tục lặp cho đến khi điều kiện trở thành False.
# Cú pháp cơ bản của vòng lặp while:
# while <điều kiện>:
i = 1  # Khởi tạo biến i với giá trị ban đầu
while i <= 5:
    print("Số hiện tại là:", i)
    i += 1  # Tăng giá trị của i lên 1 sau mỗi lần lặp

